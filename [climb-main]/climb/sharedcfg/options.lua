CLIMBConfig = {} -- Global variable for easy referencing. 

CLIMBConfig.MoneyUiEnabled = false; -- Set to false to disable Money in the top right corner. 
CLIMBConfig.SurvivalUiEnabled = false; -- Controls the UI under the healthbar.
CLIMBConfig.EnableComa = false; -- Controls the CLIMB coma on death.
CLIMBConfig.EnableFoodAndWater = false; -- Controls the food and water system.
CLIMBConfig.EnableHealthRegen = true; -- Controls the health regen. (Whether they regen health after taking damage do not disable if coma is enabled.)
CLIMBConfig.EnableBuyVehicles = false; -- Enables ability to buy vehicles from the RageUI Garages.  
CLIMBConfig.LoadPreviews = false; -- Controls the car previews with the RageUI Garages.
CLIMBConfig.VehicleStoreRadius = 250; -- Controls radius a vehicle can be stored from.
CLIMBConfig.AdminCoolDown = true; -- Enables an admin cooldown on call admin.
CLIMBConfig.AdminCooldownTime = 60; -- 1 minute in (seconds) duration of cooldown. 
CLIMBConfig.StoreWeaponsOnDeath = true; -- Stores the players weapon on death allowing them to be looted.
CLIMBConfig.DoNotDisplayIps = true; -- Removes all CLIMB related references in the console to player ip addresses.
CLIMBConfig.LoseItemsOnDeath = true; -- Controls whether you lose inventory items on death.
CLIMBConfig.AllowMoreThenOneCar = false; -- Controls if you can have more than one car out.
CLIMBConfig.F10System = true; -- Logs warnings and can be accessed via F10 (Thanks to Rubbertoe98) (https://github.com/rubbertoe98/FiveM-Scripts/tree/master/climb_punishments)
CLIMBConfig.ServerName = "CLIMB" -- Controls the name that is displayed on warnings title etc.
CLIMBConfig.PlayerSavingTime = 3000 -- Time in milliseconds to update Player saving
---------------
CLIMBConfig.LootBags = false; -- Enables loot bags and disables looting. 
CLIMBConfig.DisplayNamelLootbag = false; -- Enables notification of who's lootbag you have opened
